class Solution
{
    //Function to rotate an array by d elements in counter-clockwise direction. 
    static void rotateArr(int nums[], int d, int n)
    {
        /*
        for eg: arr = [1,2,3,4,5] k=2, 
        Now reverse the array. It becomes [5,4,3,2,1]
        Now split into two parts based on k value. like [5,4,3] and [2,1]
        Now reverse the both parts.
*/
        d =  d%nums.length;
        reverse(nums,0,nums.length-1);
        reverse(nums,0,n-d-1);
        reverse(nums,n-d,nums.length-1);
    }
    public static void reverse(int []arr,int start,int end){
        while(start<end){
            int temp = arr[start];
            arr[start] = arr[end];
            arr[end] = temp;
            start++;
            end--;
        }
    }
}